package com.example.listaescuelas;

public class Escuela {
	private String idesc;
	private String namesc;
	private String cantesc;
	private String alumnosmatriculadosvirtualesc;
	private String alumnosmatriculadospresencialesc;
  	private String alumnosnomatriculadosesc;

	public String getIdesc() {
		return idesc;
	}
	public void setIdesc(String idesc) {
		this.idesc = idesc;
	}
	public String getNamesc() {
		return namesc;
	}
	public void setNamesc(String namesc) {
		this.namesc = namesc;
	}
	public String getCantesc() {
		return cantesc;
	}
	public void setCantesc(String cantesc) {
		this.cantesc = cantesc;
	}
	public String getAlumnosmatriculadosvirtualesc() {
		return alumnosmatriculadosvirtualesc;
	}
	public void setAlumnosmatriculadosvirtualesc(
			String alumnosmatriculadosvirtualesc) {
		this.alumnosmatriculadosvirtualesc = alumnosmatriculadosvirtualesc;
	}
	public String getAlumnosmatriculadospresencialesc() {
		return alumnosmatriculadospresencialesc;
	}
	public void setAlumnosmatriculadospresencialesc(
			String alumnosmatriculadospresencialesc) {
		this.alumnosmatriculadospresencialesc = alumnosmatriculadospresencialesc;
	}
	public String getAlumnosnomatriculadosesc() {
		return alumnosnomatriculadosesc;
	}
	public void setAlumnosnomatriculadosesc(String alumnosnomatriculadosesc) {
		this.alumnosnomatriculadosesc = alumnosnomatriculadosesc;
	}
}
