package com.example.proyec1;

public class ModSemestre {

	private int idsemester;
	private int nombre_semestre;
	private int fecha_inicio;
	private int fecha_final;
	private int numero_creditos;
	private int nombre_tutor;

	public int getIdsemester() {
		return idsemester;
	}
	public void setIdsemester(int idsemester) {
		this.idsemester = idsemester;
	}
	public int getNombre_semestre() {
		return nombre_semestre;
	}
	public void setNombre_semestre(int nombre_semestre) {
		this.nombre_semestre = nombre_semestre;
	}
	public int getFecha_inicio() {
		return fecha_inicio;
	}
	public void setFecha_inicio(int fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}
	public int getFecha_final() {
		return fecha_final;
	}
	public void setFecha_final(int fecha_final) {
		this.fecha_final = fecha_final;
	}
	public int getNumero_creditos() {
		return numero_creditos;
	}
	public void setNumero_creditos(int numero_creditos) {
		this.numero_creditos = numero_creditos;
	}
	public int getNombre_tutor() {
		return nombre_tutor;
	}
	public void setNombre_tutor(int nombre_tutor) {
		this.nombre_tutor = nombre_tutor;
	}

}
